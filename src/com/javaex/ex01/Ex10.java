package com.javaex.ex01;

public class Ex10 {

	public static void main(String[] args) {

		// 증감 연산자
		int a;
		a=7;
		
		System.out.println(a);
		System.out.println(++a);
		System.out.println(a);
		System.out.println(++a);
		System.out.println(a);
		System.out.println(--a);
		System.out.println(a);
		System.out.println(--a);
		System.out.println(--a);
		System.out.println(a);
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}

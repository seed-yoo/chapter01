package com.javaex.ex01;

public class Ex11 {

	public static void main(String[] args) {

		// 증감연산자
		int b = 10;
		
		System.out.println(b);
		System.out.println(b++);
		System.out.println(b++);
		System.out.println(b);
		System.out.println(b--);
		System.out.println(b);
		
		System.out.println("========================================");
		
		int no = 10;
		
		no++;
		++no;
		System.out.println(no);
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
